#!/usr/bin/env python3

# =============================================================================
#                                DICCIONARIOS
# =============================================================================
#
# Un diccionario es una estructura de datos que almacena información usando
# pares CLAVE : VALOR.
#
#              clave  -> valor
#              "Ana"  -> 25
#              "Luis" -> 32
#
# Piensa en una agenda:
#
#              Nombre -> Edad
#
# Cada clave debe ser única dentro del diccionario.
#
# =============================================================================

edades = {
    "Ana": 25,
    "Luis": 32,
    "Carlos": 18
}


# =============================================================================
#                            CRUD DE DICCIONARIOS
# =============================================================================
#
# CRUD significa:
#
#     C -> Create (Crear)
#     R -> Read   (Leer)
#     U -> Update (Modificar)
#     D -> Delete (Eliminar)
#
# Son las 4 operaciones básicas que harás en cualquier diccionario.
#
# =============================================================================


# =============================================================================
# READ (LEER)
# =============================================================================
#
# Para obtener un valor usamos:
#
#     diccionario["clave"]
#
# Ejemplo:
#
#     print(edades["Luis"])
#
# Salida:
#
#     32
#
# =============================================================================


# =============================================================================
# CREATE (AGREGAR)
# =============================================================================
#
# Si la clave NO existe, Python la crea automáticamente.
#
# Ejemplo:
#
#     edades["Maria"] = 40
#
# Resultado:
#
#     {
#         'Ana': 25,
#         'Luis': 32,
#         'Carlos': 18,
#         'Maria': 40
#     }
#
# =============================================================================


# =============================================================================
# UPDATE (MODIFICAR)
# =============================================================================
#
# Si la clave YA existe, Python reemplaza el valor anterior.
#
# Ejemplo:
#
#     edades["Ana"] = 26
#
# Resultado:
#
#     {
#         'Ana': 26,
#         'Luis': 32,
#         'Carlos': 18
#     }
#
# =============================================================================


# =============================================================================
# DELETE (ELIMINAR)
# =============================================================================
#
# Utilizamos la palabra reservada:
#
#     del
#
# Ejemplo:
#
#     del edades["Carlos"]
#
# Resultado:
#
#     {
#         'Ana': 25,
#         'Luis': 32
#     }
#
# =============================================================================



# =============================================================================
#                           MÉTODOS MÁS UTILIZADOS
# =============================================================================
#
# Python proporciona varios métodos para trabajar con diccionarios.
#
# =============================================================================

carrito = {
    "pan": 1.50,
    "leche": 2.20,
    "cafe": 4.50
}


# =============================================================================
# .keys()
# =============================================================================
#
# Devuelve únicamente las claves.
#
# Ejemplo:
#
#     print(carrito.keys())
#
# Salida:
#
#     dict_keys(['pan', 'leche', 'cafe'])
#
# =============================================================================


# =============================================================================
# .values()
# =============================================================================
#
# Devuelve únicamente los valores.
#
# Ejemplo:
#
#     print(carrito.values())
#
# Salida:
#
#     dict_values([1.5, 2.2, 4.5])
#
# =============================================================================


# =============================================================================
# .items()
# =============================================================================
#
# Devuelve pares (clave, valor).
#
# Es el método más utilizado cuando recorremos diccionarios con for.
#
# Ejemplo:
#
#     print(carrito.items())
#
# Salida:
#
#     dict_items([
#         ('pan', 1.5),
#         ('leche', 2.2),
#         ('cafe', 4.5)
#     ])
#
# =============================================================================


# =============================================================================
# RECORRER UN DICCIONARIO CON for
# =============================================================================
#
# Gracias a .items() podemos obtener la clave y el valor al mismo tiempo.
#
# Ejemplo:
#
#     for producto, precio in carrito.items():
#         print(producto, precio)
#
# Salida:
#
#     pan 1.5
#     leche 2.2
#     cafe 4.5
#
# =============================================================================
